public class IfElse_Java {
    public static void main(String[] args) {
        int a = 17;
        int b = 17;
        if(a < b){
            System.out.println("a nhỏ hơn b ");
        }
        else if( a == b){
            System.out.println("a bằng b");
        }
        else{
            System.out.println("a lớn hơn b");
        }
    }
}
